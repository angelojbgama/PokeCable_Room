_NATIONAL_BY_NAME = {name: national_id for national_id, name in SPECIES_NAMES_BY_NATIONAL.items()}

GEN3_INTERNAL_TO_NATIONAL: dict[int, int] = {species_id: species_id for species_id in range(1, 252)}
GEN3_INTERNAL_TO_NATIONAL.update(
    {
        internal_id: _NATIONAL_BY_NAME[name]
        for internal_id, name in _GEN3_INTERNAL_NAMES.items()
        if name in _NATIONAL_BY_NAME
    }
)
NATIONAL_TO_GEN3_INTERNAL: dict[int, int] = {}
for internal_id, national_id in GEN3_INTERNAL_TO_NATIONAL.items():
    NATIONAL_TO_GEN3_INTERNAL.setdefault(national_id, internal_id)


def native_to_national(generation: int, native_species_id: int) -> int:
    tables = {
        1: GEN1_INTERNAL_TO_NATIONAL,
        2: GEN2_ID_TO_NATIONAL,
        3: GEN3_INTERNAL_TO_NATIONAL,
    }
    return tables[int(generation)][int(native_species_id)]


def national_to_native(generation: int, national_dex_id: int) -> int:
    tables = {
        1: NATIONAL_TO_GEN1_INTERNAL,
        2: NATIONAL_TO_GEN2_ID,
        3: NATIONAL_TO_GEN3_INTERNAL,
    }
    return tables[int(generation)][int(national_dex_id)]


def species_exists_in_generation(national_dex_id: int, generation: int) -> bool:
    return int(national_dex_id) in {
        1: NATIONAL_TO_GEN1_INTERNAL,
        2: NATIONAL_TO_GEN2_ID,
        3: NATIONAL_TO_GEN3_INTERNAL,
    }[int(generation)]
